package br.edu.ifmt.cba.alphalab.entity.laboratorio;

import java.util.List;

public class Curso {
	private String nome;
	private List<EnumDisciplina> disciplinas;

	/**
	 * @return the nome
	 */
	public String getNome() {
		return nome;
	}

	/**
	 * @param nome
	 *            the nome to set
	 */
	public void setNome(String nome) {
		this.nome = nome;
	}

	/**
	 * @return the disciplinas
	 */
	public List<EnumDisciplina> getDisciplinas() {
		return disciplinas;
	}

	/**
	 * @param disciplinas
	 *            the disciplinas to set
	 */
	public void setDisciplinas(List<EnumDisciplina> disciplinas) {
		this.disciplinas = disciplinas;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((nome == null) ? 0 : nome.hashCode());
		return result;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Curso other = (Curso) obj;
		if (nome == null) {
			if (other.nome != null)
				return false;
		} else if (!nome.equals(other.nome))
			return false;
		return true;
	}
}